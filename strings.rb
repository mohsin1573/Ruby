# Strings
phrase = "Mohsin Rk"
puts phrase.upcase() #To Print upper case letters
puts phrase.downcase() #To Print down case letters
puts phrase.strip() #Leading and trailing whitespace gets deleted
puts phrase.length() #To check the number of letters(Space include)
puts phrase.include? "Rk" #To check if a word is present in string or not
puts phrase[0] #To Access the single letter through index number
puts phrase[0,3] #To Access the range of letters through index number
puts phrase.index("R") #To Access the index number of a letter
